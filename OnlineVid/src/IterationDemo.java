
public class IterationDemo {
	public static void main(String args[])
	{
		//while, do-while, for, foreach
		
		int i=1;
		while(i<=5)
		{
			System.out.print("RP ");
			i++;
		}
		System.out.print(" ");
		//do while loop
		int j=1;
		do 
		{
			System.out.print("PRA ");
			j++;
		}while(j<=5);
		
		System.out.print(" ");
		//for loop
		for(int a=1; a<=5; a++)
		{
			System.out.print("REN ");
		}
		
	}

}
