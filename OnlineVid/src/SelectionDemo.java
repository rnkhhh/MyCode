
public class SelectionDemo {

	public static void main(String args[])
	{
		//if elseif example
		int n=0;
		if(n==0)
		{
			System.out.println("Nothing");
			System.out.println("Else");
		}
		else if(n%2==0)
		{
			System.out.println("Even");
		}
		else
		{
			System.out.println("Odd");
		}
	}
}
